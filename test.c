int main()
{
	int arr[] = { 9,5,8,7,6,4,1,2,3 };
	int i, j;
	int n = sizeof(arr) / sizeof(arr[0]);
	for (i = 0; i < n; i++)
	{
		for (j = 0; j < n - i - 1; j++)
		{
			if (arr[j] > arr[j + 1])
			{
				int tmp = arr[j];
				arr[j] = arr[j + 1];
				arr[j + 1] = tmp;
			}
		}
	}
	int left = 0;
	int right = n - 1;
	int pp[9] = { 0 };
	int t = 0;
	while (left <= right)
	{
		pp[t] = arr[left];
		t++;
		pp[t] = arr[right];
		t++;
		left++;
		right--;
	}
	for (i = 0; i < 9; i++)
	{
		printf("%d ", pp[i]);
	}

	return 0;
}